// Basic service worker for offline caching
const CACHE_NAME = 'resus-v1';
const PRECACHE_URLS = [
  '/',
  '/algorithms/index.json',
  '/algorithms/algo_paed_bls_out.json',
  '/algorithms/algo_paed_bls_in.json',
  '/algorithms/algo_newborn_ls.json',
  '/algorithms/algo_anaphylaxis.json',
  '/algorithms/algo_paed_fbao.json',
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(PRECACHE_URLS))
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((names) =>
      Promise.all(
        names
          .filter((name) => name !== CACHE_NAME)
          .map((name) => caches.delete(name))
      )
    )
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  event.respondWith(
    caches.match(event.request).then((cached) => {
      if (cached) return cached;
      return fetch(event.request).then((response) => {
        if (!response || response.status !== 200 || response.type !== 'basic') {
          return response;
        }
        const responseClone = response.clone();
        caches.open(CACHE_NAME).then((cache) => {
          cache.put(event.request, responseClone);
        });
        return response;
      });
    })
  );
});
